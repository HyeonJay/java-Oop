package syntax;

public class SwingSyntax {
	// 스윙( swing )은 AWT의 제약을 극복하기 위하여 만들어졌다
	// 자바에서 GUI를 구현하기 위해 JDK에서 기본적으로 제공하는
	// 개발 툴킷으로 자바기반의 클래스의 일부이다.

	// 스윙은 기존의 발표되었던 AWT( Abstract Window Toolkit )
	// 가 OS및 윈도우 시스템의 자원을 그대로 제공받기 위해
	// 자비에서 지향하는 "Write Once, Run Ecerywhere( WORE )"를 
	// 구현하기 위해 각종 시스템에서 공통적으로 제공하는 버튼,
	// 대화창들로만 구현하고 표나 트리등의 좀 더 복잡하고
	// 다양한 그래픽 컴포넌트를 사용할 수 없다는
	// 단점을 보완하기 위해 JDK1.2버전부터 사용 했다.
}
